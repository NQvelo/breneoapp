/**
 * Generates public/firebase-messaging-sw.js from VITE_FIREBASE_* env vars.
 * - Production: imported into the main PWA service worker via Workbox importScripts.
 * - Development: registered directly (vite-plugin-pwa devOptions.enabled is false).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import dotenv from "dotenv";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

dotenv.config({ path: path.join(root, ".env") });
dotenv.config({ path: path.join(root, ".env.local"), override: true });

const config = {
  apiKey: process.env.VITE_FIREBASE_API_KEY?.trim() || "",
  authDomain: process.env.VITE_FIREBASE_AUTH_DOMAIN?.trim() || "",
  projectId: process.env.VITE_FIREBASE_PROJECT_ID?.trim() || "",
  storageBucket: process.env.VITE_FIREBASE_STORAGE_BUCKET?.trim() || "",
  messagingSenderId: process.env.VITE_FIREBASE_MESSAGING_SENDER_ID?.trim() || "",
  appId: process.env.VITE_FIREBASE_APP_ID?.trim() || "",
  measurementId: process.env.VITE_FIREBASE_MEASUREMENT_ID?.trim() || "",
};

const hasConfig = Boolean(
  config.apiKey &&
    config.projectId &&
    config.messagingSenderId &&
    config.appId,
);

const outputPath = path.join(root, "public/firebase-messaging-sw.js");

if (!hasConfig) {
  const stub = `/* Firebase messaging — set VITE_FIREBASE_* in .env and rerun npm run dev */\n`;
  fs.writeFileSync(outputPath, stub, "utf8");
  console.warn(
    "[firebase-sw] VITE_FIREBASE_* not set; wrote stub firebase-messaging-sw.js",
  );
  process.exit(0);
}

const contents = `/* Generated by scripts/write-firebase-sw.mjs — do not edit manually */
importScripts("https://www.gstatic.com/firebasejs/12.9.0/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/12.9.0/firebase-messaging-compat.js");

firebase.initializeApp(${JSON.stringify(config, null, 2)});

const messaging = firebase.messaging();

messaging.onBackgroundMessage(function (payload) {
  const notification = payload.notification || {};
  const data = payload.data || {};
  const title = notification.title || data.title || "Breneo";
  const body = notification.body || data.body || "";
  const icon = notification.icon || data.icon || "/lovable-uploads/Breneo-logo.png";
  const url = data.url || data.link || "/notifications";

  return self.registration.showNotification(title, {
    body: body,
    icon: icon,
    badge: "/lovable-uploads/Breneo-logo.png",
    tag: data.tag || "breneo-fcm",
    data: { url: url },
  });
});

self.addEventListener("notificationclick", function (event) {
  event.notification.close();
  var url =
    (event.notification.data && event.notification.data.url) || "/notifications";
  var fullUrl = new URL(url, self.location.origin).href;

  event.waitUntil(
    self.clients
      .matchAll({ type: "window", includeUncontrolled: true })
      .then(function (clientList) {
        for (var i = 0; i < clientList.length; i++) {
          var client = clientList[i];
          if ("focus" in client) {
            if ("navigate" in client) {
              return client.navigate(fullUrl).then(function (focused) {
                return focused.focus();
              });
            }
            return client.focus();
          }
        }
        if (self.clients.openWindow) {
          return self.clients.openWindow(fullUrl);
        }
      }),
  );
});
`;

fs.writeFileSync(outputPath, contents, "utf8");
console.log("[firebase-sw] Wrote public/firebase-messaging-sw.js");
